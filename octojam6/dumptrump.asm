again

: delay-one-second
# clobbers v4
# Chip-8 delay timer runs at 60 Hz
v4 := 60
delay := v4

loop
	v4 := delay

if v4 != 0 then again

return

: delay-frac-second
# v3 is how long to delay
delay := v3
loop
v3 := delay
if v3 == 0 then return
again

